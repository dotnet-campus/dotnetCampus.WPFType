namespace System.Windows
{
    /// <summary>
    /// This property determines the appearance of the list item's marker
    /// </summary>
    public enum TextMarkerStyle
    {
        /// <summary>
        /// No marker
        /// </summary>
        None,

        /// <summary>
        /// Solid disc circle
        /// </summary>
        Disc,

        /// <summary>
        /// Hallow disc circle
        /// </summary>
        Circle,

        /// <summary>
        /// Hallow square shape
        /// </summary>
        Square,

        /// <summary>
        /// Solid square shape
        /// </summary>
        Box,

        /// <summary>
        /// Lower roman letter e.g. i, ii, iii, iv, etc.
        /// </summary>
        LowerRoman,

        /// <summary>
        /// Upper roman letter e.g. I, II, III, IV, etc.
        /// </summary>
        UpperRoman,

        /// <summary>
        /// Lowercase ascii e.g. a, b, c, etc.
        /// </summary>
        LowerLatin,

        /// <summary>
        /// Uppercase ascii e.g. A, B, C, etc.
        /// </summary>
        UpperLatin,

        /// <summary>
        /// Decimal numbers, beginning with 1
        /// </summary>
        Decimal,
    }
}